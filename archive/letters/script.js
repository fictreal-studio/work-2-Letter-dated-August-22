const form = document.getElementById('letters-form');
const input = document.getElementById('letters-pass');
const message = document.getElementById('auth-message');
const lockPanel = document.getElementById('lock-panel');
const archivePanel = document.getElementById('archive-panel');
const list = document.getElementById('letter-list');
const card = document.getElementById('letter-card');
const dateEl = document.getElementById('letter-date');
const ageEl = document.getElementById('letter-age');
const titleEl = document.getElementById('letter-title');
const bodyEl = document.getElementById('letter-body');
const finalNote = document.getElementById('final-note');
let activeYear = letters[0].year;

function letterDate(letter) {
  return `${letter.year}年8月22日`;
}

function renderList() {
  list.innerHTML = '';
  letters.forEach((letter) => {
    const button = document.createElement('button');
    button.type = 'button';
    button.className = `year-button${letter.final ? ' is-final' : ''}`;
    button.dataset.year = letter.year;
    button.setAttribute('role', 'option');
    button.setAttribute('aria-selected', String(letter.year === activeYear));
    button.innerHTML = `<span>${letter.year}年8月22日</span><small>大樹${letter.age}歳${letter.final ? ' / 最後の手紙' : ''}</small>`;
    button.addEventListener('click', () => selectLetter(letter.year));
    list.appendChild(button);
  });
}

function renderLetter(year) {
  const letter = letters.find((item) => item.year === year) || letters[0];
  dateEl.textContent = letterDate(letter);
  ageEl.textContent = `大樹${letter.age}歳へ${letter.final ? ' / 最後の手紙' : ''}`;
  titleEl.textContent = `${letter.year}年8月22日｜大樹${letter.age}歳へ${letter.final ? '｜最後の手紙' : ''}`;
  bodyEl.textContent = letter.body;
  finalNote.hidden = !letter.final;
  card.classList.toggle('is-final', Boolean(letter.final));
  document.querySelectorAll('.year-button').forEach((button) => {
    const isActive = Number(button.dataset.year) === letter.year;
    button.classList.toggle('is-active', isActive);
    button.setAttribute('aria-selected', String(isActive));
  });
}

function selectLetter(year) {
  activeYear = year;
  card.classList.add('is-changing');
  window.setTimeout(() => {
    renderLetter(year);
    card.classList.remove('is-changing');
  }, 180);
}

function openArchive() {
  lockPanel.hidden = true;
  archivePanel.hidden = false;
  archivePanel.classList.add('is-open');
  activeYear = letters[0].year;
  renderList();
  renderLetter(activeYear);
  archivePanel.scrollIntoView({ behavior: 'smooth', block: 'start' });
}

form.addEventListener('submit', (event) => {
  event.preventDefault();
  if (input.value.trim() === PASSWORD) {
    message.textContent = '';
    message.classList.remove('is-error');
    sessionStorage.setItem('lettersArchiveUnlocked', 'true');
    openArchive();
    return;
  }
  message.textContent = 'パスワードが違います。';
  message.classList.add('is-error');
});

if (sessionStorage.getItem('lettersArchiveUnlocked') === 'true') {
  openArchive();
}
